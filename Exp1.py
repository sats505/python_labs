print("=== NUMERIC DATA TYPES ===")
# Integer
a = 10
print("Integer:", a, type(a))
# Float
b = 3.14
print("Float:", b, type(b))
# Complex Number
c = 2 + 5j
print("Complex:", c, type(c))
print("\n=== BOOLEAN DATA TYPE ===")
x = True
y = False
print("Boolean True:", x, type(x))
print("Boolean False:", y, type(y))
print("\n=== STRING DATA TYPE ===")
name = "Python Programming"
print("String:", name, type(name))
print("\n=== LIST (SEQUENCE TYPE) ===")
numbers = [1, 2, 3, 4]
print("List:", numbers, type(numbers))
print("\n=== TUPLE (SEQUENCE TYPE) ===")
coordinates = (10, 20)
print("Tuple:", coordinates, type(coordinates))
print("\n=== SET DATA TYPE ===")
unique_values = {1, 2, 3, 3, 2}
print("Set:", unique_values, type(unique_values))
print("\n=== DICTIONARY DATA TYPE ===")
student = {
"name": "Sattu",
"age": 20,
"course": "Python"
}
print("Dictionary:", student, type(student))
